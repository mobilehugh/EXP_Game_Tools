    Model: str = "No-Name-Model"
    Fabricator: str = "ACME"

    Age_Spans: list[str] = field(default_factory=list) 
    Age_Cat: str = "Moderate"
    Age: int = 1 # will convert to scientific on outputs
    Age_Suffix: str = "seconds"

    Date_Created: str = "Unmade"
    Date_Updated: str = "Unmade"
    CON_Prime: int = 1
    DEX_Prime: int = 1
    INT_Prime: int = 1
    PSTR_Prime: int = 1   
    Adapt: int = 0
    Value: int = 42
    Base_Family:str = "Debased"
    CF: int = 0
    Ramming: int = 0
    Attacks: list[str] = field(default_factory=list) 
    Defences: list[str] = field(default_factory=list)
    Peripherals: list[str] = field(default_factory=list)
    Spec_Sheet: list[str] = field(default_factory=list)

    Locomotion: str = "Treads"
    Power_Plant: str = "Batteries"
    Power_Reserve: str = "all day!"
    Sensors: list[str] = field(default_factory=list)

